    private void joinBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_joinBtnActionPerformed

        try {
            //Get text from txtfields and save it to the variables
            empName = nameTxt.getText();
            position = positionTxt.getText();
            userName = usernameTxt.getText();
            password = passwordTxt.getText();

            /**
             * call addEmployee method which is implemented in the controller
             * class values are passed by parameters validate all the fields
             * validates username
             */
            if (validateSignupContents(empName, position, userName, password) == false) {
                JOptionPane.showMessageDialog(null, "Empty Fields detected !! Please fill all Fields","ERROR",JOptionPane.ERROR_MESSAGE);
            } else if (validateUsername(userName) == false) {
                JOptionPane.showMessageDialog(null, "user name is already existing!!! please try another", "ERROR", JOptionPane.ERROR_MESSAGE);
                usernameTxt.setText(" ");
            } else {
                addEmployee(empName, position, userName, password);
                JOptionPane.showMessageDialog(null, "successfully Created");
                Login login = new Login();
                setVisible(false);
                login.setVisible(true);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Sorry some error occured please try again", "Error", JOptionPane.ERROR_MESSAGE);
            nameTxt.setText("");
            positionTxt.setText(" ");
            usernameTxt.setText("");
            passwordTxt.setText("");
        }


    }//GEN-LAST:event_joinBtnActionPerformed

