    private static boolean validateSignupContents(java.lang.String empName, java.lang.String position, java.lang.String username, java.lang.String password) {
        ebankingsystemEmployee.EBankingEmployeeWs_Service service = new ebankingsystemEmployee.EBankingEmployeeWs_Service();
        ebankingsystemEmployee.EBankingEmployeeWs port = service.getEBankingEmployeeWsPort();
        return port.validateSignupContents(empName, position, username, password);
    }

    private static boolean validateUsername(java.lang.String username) {
        ebankingsystemEmployee.EBankingEmployeeWs_Service service = new ebankingsystemEmployee.EBankingEmployeeWs_Service();
        ebankingsystemEmployee.EBankingEmployeeWs port = service.getEBankingEmployeeWsPort();
        return port.validateUsername(username);
    }

    private static void addEmployee(java.lang.String empName, java.lang.String position, java.lang.String username, java.lang.String password) {
        ebankingsystemEmployee.EBankingEmployeeWs_Service service = new ebankingsystemEmployee.EBankingEmployeeWs_Service();
        ebankingsystemEmployee.EBankingEmployeeWs port = service.getEBankingEmployeeWsPort();
        port.addEmployee(empName, position, username, password);
    }

    private static void deleteEmployee(java.lang.String username, java.lang.String password) {
        ebankingsystemEmployee.EBankingEmployeeWs_Service service = new ebankingsystemEmployee.EBankingEmployeeWs_Service();
        ebankingsystemEmployee.EBankingEmployeeWs port = service.getEBankingEmployeeWsPort();
        port.deleteEmployee(username, password);
    }

    private static void updateEmployee(java.lang.String empName, java.lang.String position, java.lang.String username, java.lang.String password) {
        ebankingsystemEmployee.EBankingEmployeeWs_Service service = new ebankingsystemEmployee.EBankingEmployeeWs_Service();
        ebankingsystemEmployee.EBankingEmployeeWs port = service.getEBankingEmployeeWsPort();
        port.updateEmployee(empName, position, username, password);
    }

   
   
